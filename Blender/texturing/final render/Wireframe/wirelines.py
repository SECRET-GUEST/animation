#Function to program all actions

def main():
    #Convert the hex color to RGB
    wire_color_rgb = hex_to_rgb(wire_color_hex)

    #Setup the wireframe material with the specified color and glow intensity
    wire_mat = setup_wireframe_materials(wire_color_rgb, glow_intensity)

    #Create a new view layer for the wireframe
    wireframe_view_layer = create_wireframe_view_layer()


    for obj in bpy.context.scene.objects:
        if obj.type == "MESH":

            #Add a wireframe modifier to the mesh
            create_wireframe_modifier(obj)

            #Apply the wireframe material to the mesh
            apply_wireframe_material(obj, wire_mat)




if __name__ == "__main__":
    main()
