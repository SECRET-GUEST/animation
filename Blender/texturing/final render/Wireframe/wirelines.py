#Define variable for the background
world = bpy.context.scene.world

#Modify background in choosen color
world.use_nodes = True
bg_node = world.node_tree.nodes.new(type='ShaderNodeBackground')
bg_node.inputs[0].default_value = background_color
world.node_tree.links.new(bg_node.outputs[0], world.node_tree.nodes['World Output'].inputs['Surface'])




#Function toConvert hex color code to RGB tuple
def hex_to_rgb(hex_color):
    hex_color = hex_color.lstrip("#")
    return tuple(int(hex_color[i:i+2], 16) for i in (0, 2, 4))



#Function to create a wireframe modifier for the given object
def create_wireframe_modifier(obj):

    #control the wireframe appearance, such as thickness and offset :
    wireframe_mod = obj.modifiers.new("Wireframe", "WIREFRAME")
    wireframe_mod.use_replace = False
    wireframe_mod.material_offset = 1
    wireframe_mod.thickness = the_thickness
    wireframe_mod.use_even_offset = False #You can comment this line if you don't have issues with thickness "even" box in "modifiers"



#Function to create a new material and enable node-based editing
def setup_wireframe_materials(wire_color_rgb, glow_intensity):

    mat = bpy.data.materials.new("Wireframe_Material")
    mat.use_nodes = True

    #Access the nodes and links of the material's node tree
    nodes = mat.node_tree.nodes
    links = mat.node_tree.links

    #Clear any nodes
    nodes.clear()


    #Create a node for the material's output
    node_output = nodes.new(type="ShaderNodeOutputMaterial")
    node_output.location = (700, 0)

    #Create a node for the emission shader, which creates the wireframe glow
    node_emission = nodes.new(type="ShaderNodeEmission")
    node_emission.location = (400, 0)
    node_emission.inputs[1].default_value = float(glow_intensity) / 10          #controls the intensity of the wireframe glow


    #Create a node for the mix shader, which combines the wireframe and emission shaders
    node_mix = nodes.new(type="ShaderNodeMixShader")
    node_mix.location = (200, 0)


    #Create a node for the shader, which generates the  lines
    #It will control the appearance of the wireframe, such as thickness and pixel size
    node_wireframe = nodes.new(type="ShaderNodeWireframe")
    node_wireframe.location = (0, 0)
    node_wireframe.use_pixel_size = True
    node_wireframe.inputs[0].default_value = 0.01


    #Create a node for the RGB color of the wireframe lines
    node_color = nodes.new(type="ShaderNodeRGB")
    node_color.location = (-200, 0)

    #Then convert the wireframe color from RGB tuple to 0-1 range and set as default value
    node_color.outputs[0].default_value = tuple(c / 255 for c in wire_color_rgb) + (1,)


    #Connects the nodes together to form the material's node tree
    links.new(node_color.outputs[0], node_emission.inputs[0])
    links.new(node_emission.outputs[0], node_mix.inputs[2])
    links.new(node_wireframe.outputs[0], node_mix.inputs[0])
    links.new(node_mix.outputs[0], node_output.inputs[0])

    return mat


#Function to 
def apply_wireframe_material(obj, wire_mat):
   
    #So check if the object has any materials applied
    if len(obj.data.materials) == 0:

        #If not, append None to the list of materials
        obj.data.materials.append(None)

    #Then append the wireframe to the list 
    obj.data.materials.append(wire_mat)



#Function to 
def create_wireframe_view_layer():

    #Get the view layers of the current scene
    view_layers = bpy.context.scene.view_layers

    #Create a new view layer named "Wireframe"
    wireframe_view_layer = view_layers.new("Wireframe")

    return wireframe_view_layer




